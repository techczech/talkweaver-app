import { readFileSync, writeFileSync } from 'node:fs'
import { pathToFileURL } from 'node:url'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import ts from 'typescript'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const entriesPath = join(root, 'src/shared/layout-registry/entries.ts')
export const generatedPath = join(root, 'compiler/scripts/lib/trigger-dictionary.generated.mjs')

async function loadRegistry() {
  const source = readFileSync(entriesPath, 'utf8')
  const js = ts.transpileModule(source, {
    compilerOptions: { module: ts.ModuleKind.ESNext, target: ts.ScriptTarget.ES2022 }
  }).outputText
  return import(`data:text/javascript;base64,${Buffer.from(js).toString('base64')}`)
}

function targetFor(entry) {
  if (entry.resolvesTo) return entry.resolvesTo
  if (entry.kind === 'layout') return { key: 'layout', value: entry.name }
  return { key: entry.name, value: true }
}

export async function generateTriggerDictionarySource() {
  const { GLOBAL_OPTION_GROUPS, LAYOUTS } = await loadRegistry()
  const dictionary = {}
  const dynamicPatterns = []
  const valueTriggers = {}
  const collectValueTriggers = (groups) => {
    for (const group of groups ?? []) {
      const tokens = [
        ...(group.dictionaryTokens ?? []),
        ...(group.values ?? []).map((option) => option.token)
      ]
      for (const token of tokens) {
        const at = String(token ?? '').indexOf('=')
        if (at <= 0) continue
        const key = token.slice(0, at)
        const value = token.slice(at + 1)
        valueTriggers[key] ??= []
        if (!valueTriggers[key].includes(value)) valueTriggers[key].push(value)
      }
    }
  }
  for (const entry of LAYOUTS) {
    const target = targetFor(entry)
    for (const word of entry.triggerWords) dictionary[word] = target
    for (const pattern of entry.dynamicPatterns ?? []) dynamicPatterns.push(pattern)
    collectValueTriggers(entry.options)
  }
  collectValueTriggers(GLOBAL_OPTION_GROUPS)
  return `// Generated by scripts/generate-trigger-dictionary.mjs. Do not edit.\n` +
    `export const TRIGGER_DICTIONARY = ${JSON.stringify(dictionary, null, 2)}\n\n` +
    `export const DYNAMIC_PATTERNS = ${JSON.stringify(dynamicPatterns, null, 2)}\n\n` +
    `export const VALUE_TRIGGER_DICTIONARY = ${JSON.stringify(valueTriggers, null, 2)}\n`
}

export async function writeTriggerDictionary() {
  writeFileSync(generatedPath, await generateTriggerDictionarySource())
}

if (process.argv[1] && pathToFileURL(process.argv[1]).href === import.meta.url) {
  await writeTriggerDictionary()
}
